function translateName(name) {
  if (currentLang === 'zh') return name;
  return CITY_MAP[name] || COUNTRY_MAP[name] || name;
}

// Current language — read from URL param, then localStorage, then default to English
var currentLang = (function() {
  const params = new URLSearchParams(window.location.search);
  const urlLang = params.get('lang');
  if (['zh','en','ar','ja'].includes(urlLang)) return urlLang;
  const stored = localStorage.getItem('bazu-lang');
  if (['zh','en','ar','ja'].includes(stored)) return stored;
  return 'en';
})();

function t(key) {
  return (I18N[currentLang] && I18N[currentLang][key]) || key;
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('bazu-lang', lang);
  applyLang();
}

function applyLang() {
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const text = t(key);
    if (text) el.textContent = text;
  });

  // Update all placeholders with data-i18n-ph attribute
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    const text = t(key);
    if (text) el.placeholder = text;
  });

  // Update language toggle button
  const langBtn = document.getElementById('langToggle');
  if (langBtn) {
    const span = langBtn.querySelector('span');
    if (span) span.textContent = currentLang === 'zh' ? 'EN' : '中文';
  }

  // Translate country/city select options (skip options with data-i18n)
  document.querySelectorAll('#regCountry option, #editCountry option').forEach(opt => {
    if (opt.hasAttribute('data-i18n')) return; // handled by data-i18n above
    const zh = opt.getAttribute('data-zh') || opt.textContent;
    if (!opt.getAttribute('data-zh')) opt.setAttribute('data-zh', zh);
    opt.textContent = translateName(zh);
  });

  // Re-populate city dropdowns to reflect translated names
  if (typeof populateRegCities === 'function') populateRegCities();
  if (typeof populateCities === 'function') {
    const editCountry = document.getElementById('editCountry');
    if (editCountry) populateCities(editCountry.value, document.getElementById('editCity')?.value || '');
  }

  // Update html lang attribute & direction
  const langMap = { zh: 'zh-CN', en: 'en', ar: 'ar', ja: 'ja' };
  document.documentElement.lang = langMap[currentLang] || 'en';
  document.documentElement.dir = currentLang === 'ar' ? 'rtl' : 'ltr';

  // Update SEO meta tags
  const metaTitle = t('metaTitle');
  const metaDesc = t('metaDescription');
  const metaKw = t('metaKeywords');
  const ogTitle = t('ogTitle');
  const ogDesc = t('ogDescription');
  const setMeta = (sel, content) => { const el = document.querySelector(sel); if (el) el.setAttribute('content', content); };
  setMeta('title', metaTitle);
  document.title = metaTitle;
  setMeta('meta[name="description"]', metaDesc);
  setMeta('meta[name="keywords"]', metaKw);
  setMeta('meta[property="og:title"]', ogTitle);
  setMeta('meta[property="og:description"]', ogDesc);
  setMeta('meta[property="og:locale"]', currentLang === 'zh' ? 'zh_CN' : 'en_US');
}

